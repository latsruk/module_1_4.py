my_string = input('Введите произвольный текст: ') # Создал переменную и присвоил значение
print(len(my_string)) # выводим количество символов введённого текста
print(my_string.upper()) # Вывожу строку в верхнем регистре
print(my_string.lower()) # Вывожу строку в нижнем регистре
print(my_string.replace(' ', '')) # Ввёл команду для удаления пробелов
print(my_string[0]) # Вывожу первый символ строки
print(my_string[-1]) # Вывожу второй символ строки